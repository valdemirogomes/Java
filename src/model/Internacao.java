package model;
public class Internacao{
     private String localInternação;
     private int numero;
    public Internacao(String localInternação, int numero) {
        this.localInternação = localInternação;
        this.numero = numero;
    }
    public String getLocalInternação() {
        return localInternação;
    }
    public void setLocalInternação(String localInternação) {
        this.localInternação = localInternação;
    }
    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }
    
   
    
}



            
            
                
    

    

    
